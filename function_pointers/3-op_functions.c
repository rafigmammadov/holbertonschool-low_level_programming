#include "3-calc.h"


/**
 * op_add - Function that adds two integers
 *
 * @a: The parameter that represents integer
 * @b: The parameter that represents integer
 *
 * Return: Returns the sum of a and b
 *
 *
 */


int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Function that substracts two integers
 *
 * @a: The parameter that represents integer
 * @b: The parameter that represents integer
 *
 * Return: Returns the difference of a and b
 *
 *
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - Function that multiplies two integers
 *
 * @a: The parameter that represents integer
 * @b: The parameter that represents integer
 *
 * Return: Returns the product of a and b
 *
 *
 */


int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - Function that divides two integers
 *
 * @a: The parameter that represents integer
 * @b: The parameter that represents integer
 *
 * Return: Returns the result of the division of a by b
 *
 *
 */


int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - Function that calculates remainder of two integers
 *
 * @a: The parameter that represents integer
 * @b: The parameter that represents integer
 *
 * Return: Returns the remainder of the division of a by b
 *
 *
 */


int op_mod(int a, int b)
{
	return (a % b);
}
